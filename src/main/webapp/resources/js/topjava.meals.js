function filter() {
    var form = $('#filterForm');

    $.get(context.ajaxUrl + "filter", form.serialize(), function (data) {
        updateFilterTable(data);
    });
}

function updateFilterTable(data) {
    context.datatableApi.clear().rows.add(data).draw();
}